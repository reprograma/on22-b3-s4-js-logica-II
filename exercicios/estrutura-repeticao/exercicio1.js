/**Exercício 1:**
**Instruções:** Crie um array com 5 números. Em seguida crie um `for` que imprima cada número do array no console.

_Dica: Utilize a propriedade length do array para definir a condição de parada do loop.*/

const number = [2, 4, 6, 7, 8]
for (let i = 0; i < number.length; i++) {
    console.log(number[i])
}
